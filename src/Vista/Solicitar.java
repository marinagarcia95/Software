        public void borrarCr(){
            int resp= JOptionPane.showConfirmDialog(this, "Desea Borrar","por favor confirme",JOptionPane.YES_NO_OPTION);
        if(resp==JOptionPane.YES_OPTION){
            int fila = tblSolicitar.getSelectedRow();
            cli.borrarCr(fila);
            mostrar();
            limpiar();
        }
 
}
          public void buscar(){
         int dni = Integer.parseInt(this.TxtBDNI.getText());
         boolean encontrado= fin.buscarCliente(dni);
         if(encontrado==false){
             JOptionPane.showMessageDialog(this, "No se encontro");
         }
         else{
          mostrar();   
         }
     }
