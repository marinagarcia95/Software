     public void agregarCr(){
     
         
           String Modalidad = comboPlan.getSelectedItem().toString();
           String pagado= comboPlan.getSelectedIndex()==0?"SI":"NO";
           double monto =  Double.parseDouble(txtMonto.getText());
           int cuotas = Integer.parseInt(txtCuo.getText());
           double interes =  Double.parseDouble(txtInt.getText());
           //double totalCr= Double.parseDouble(cred.calcularTotal());
           //double totalCr= cred.getTotalCr();
           Credito nuevo= new Credito(interes, new Plan(Modalidad, cuotas, monto), pagado);
           //Credito nuevo= new Credito(interes, new Plan(Modalidad, cuotas, monto), pagado, totalCr);
           nuevo.generarCuo();
           fin.getClienteActivo().agregarCr(nuevo);
        

        mostrar();
        limpiar();
     }
     
